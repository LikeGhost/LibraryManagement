package jmu.yaw.library.po;

public class BorrowType {

    private int btId;
    private String btContent;
    private double btFine;


    public int getBtId() {
        return btId;
    }

    public void setBtId(int btId) {
        this.btId = btId;
    }

    public String getBtContent() {
        return btContent;
    }

    public void setBtContent(String btContent) {
        this.btContent = btContent;
    }

    public double getBtFine() {
        return btFine;
    }

    public void setBtFine(double btFine) {
        this.btFine = btFine;
    }
}
